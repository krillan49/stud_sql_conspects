-- > sqlite3 db_name.расширение   - войти, находясь в директории с БД в данную БД(в ее рабочую текстовую консольную оболочку) например testDB.sqlite
-- > sqlite3 ./DB/leprosorium.db  - войти с указанием доп пути к директории

-- Команды внутри текстовой консольной оболочки БД:
CREATE TABLE "Some" ("Id" INTEGER PRIMARY KEY AUTOINCREMENT, "Name" VARCHAR, "Price" INTEGER); -- запрс создания новой таблицы в данной базе данных(среда позволяет писать запросы в несколько строк соотв в конце запроса(именно запроса не команды) ставится точка с запятой, иначе на другую строку переходит ввод запроса, ее можно поставить и на новой строке)
-- .tables                                               -- вывести список таблиц которые существуют вданной базе данных
SELECT * FROM Cars;                                      -- пример запроса SELECT, получаем вывод таблицы или ее элементов
-- .mode column                                          -- делает размер колонок в выводе одинаковыми
-- .headers on                                           -- Включить заголовки(похоже включена и так в .mode column)
INSERT INTO Cars (Name, Price) VALUES ('Foo', 6743);     -- пример запроса INSERT, добавим новую строку в таблицу
