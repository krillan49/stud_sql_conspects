-- 	                                1. Создание БД в sqlitebrowser:

-- новая база данных -> выбираем директорию имя фаила и расширение -> фаил базы данной создан -> в появившемся окне создаем таблицу, добавляя имя таблицы и столбцы с параметрами -> внизу создание отобразится в синтаксисе кодом нашей таблицы
CREATE TABLE "Cars" ("Id" INTEGER PRIMARY KEY AUTOINCREMENT, "Name" VARCHAR, "Price" INTEGER) -- запрос к базе данных на создание таблицы
"Cars"        -- имя таблицы
"Id"          -- первая колонка любой таблицы это обычно id, уникальный идентификатор строки
INTEGER       -- тип данных целые числа, хорошо подходит для id
PRIMARY KEY   -- значение всегда должно быть у столбца id
AUTOINCREMENT -- значение этого столбца будет автоматически увеличиваться про создании новых строк
VARCHAR       -- тип данных подразумевает что будет небольшой текст, с заданным числом символов например VARCHAR(50). Но в sqlite VARCHAR==TEXT потому лучше использовать TEXT (по умолчанию VARCHAR/TEXT поддерживают юникод(другие алфавиты) в других базах это отдельные типы NVARCHAR и NTEXT)
-- теперь наша таблица создана в этой базе данных и в основном окне в списке таблиц появилась наша таблица
-- во вкладки SQL основного окна можно делать запросы(наполнять или выводить чтото из таблицы)
-- таблицу можно посмотреть во вкладке "Данные"



